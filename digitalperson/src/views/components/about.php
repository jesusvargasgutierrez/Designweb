<div class="photo">
    El siguiente paso, sera tomar una fotografia, para ello, es necesario contar con una 
    camara web, si su computadora es de escritorio, sera necesario que sea conectada una
    camara externa. <br><br>
    Cualquier camara web externa podria funcionar. <br><br>

    si se encuentra en una computadora portatil, no es necesario conectar ninguna camara
    adicional, ya que la mayoria de estas computadoras ya cuenta con una integrada.<br><br>

    Una vez conectada la camara web en caso de ser necesario, la aplicacion solicitara 
    permisos para acceder a dicho hardware en una ventana como la siguiente:

    <img style="width:100%" src="https://i.ibb.co/2shBM4y/imagen.png" alt="image">

